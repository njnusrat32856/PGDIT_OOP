package calculator;

public class SumCalculatorClass {
	
	public int sumCalculate(int a, int b) {
        return a + b;
    }

    public int sumCalculate(int a, int b, int c) {
        return a + b + c;
    }

    public int sumCalculate(int a, int b, int c, int d) {
        return a + b + c + d;
    }

}
